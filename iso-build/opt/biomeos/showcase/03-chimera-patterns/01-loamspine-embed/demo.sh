#!/usr/bin/env bash
# Demo: loamSpine Embedded Integration
# Shows loamSpine library used directly in BiomeOS (chimera pattern)

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BIOMEOS_ROOT="$SCRIPT_DIR/../../.."

